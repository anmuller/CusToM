function y=equationBVH3(a,q)
q1=q(:,1);
q2=q(:,2);

if size(a,2)<18
    a=[a ; zeros(18-size(a,2),1)];
end

y=a(1) + a(2).*q1 + a(3).*q2 + a(4).*q1.*q2 + a(5).*q1.^2 + ...
     a(6).*q2.^2 + a(7).*q1.^2.*q2 +  a(8).*q1.*q2.^2 +  a(9).*q1.^2.*q2.^2+...
     a(10).*q1.^3 + a(11).*q1.^3.*q2 + a(12).*q1.^3.*q2^2 + a(13).*q1.^4 +...
     a(14).*q1.^4.*q2 + a(15).*q1.^4.*q2.^2 + a(16).*q1.^5+...
     a(17).*q1.^5.*q2+a(18).*q1.^5.*q2.^2;
y=y';

end

